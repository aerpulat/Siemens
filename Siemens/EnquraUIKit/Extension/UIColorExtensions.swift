import UIKit

extension UIColor {

    public class func eqDarkGray() -> UIColor {
        return UIColor(hex: "#444444")
    }

    public class func eqGray() -> UIColor {
        return UIColor(hex: "#777777")
    }

    public class func headerGray() -> UIColor {
        return UIColor(hex: "#8ea1ac")
    }

    public class func siemensDark() -> UIColor {
        return UIColor(hex: "#1a8286")
    }

    public class func siemensLight() -> UIColor {
        return UIColor(hex: "#38a09f")
    }

    public class func siemensGray() -> UIColor {
        return UIColor(hex: "#788791")
    }
    
    public class func siemensTextGray() -> UIColor {
        return UIColor(hex: "#3c464b")
    }

    public class func siemens() -> UIColor {
        return UIColor(hex: "#49a7a7")
    }

    public class func chartBarLight() -> UIColor {
        return UIColor(hex: "#50b9b8")
    }

    public class func chartBarDark() -> UIColor {
        return UIColor(hex: "#3a9498")
    }
    
    public class func rowBackgroundGray() -> UIColor {
        return UIColor(hex: "#F5F5F5")
    }
    public class func eqCrayonOrange() -> UIColor {
        return UIColor(hex: "#FF6411")
    }
    public class func eqGreenText() -> UIColor {
        return UIColor(hex: "#5aba19")
    }
    public class func eqGreenLightText() -> UIColor {
        return UIColor(hex: "#83e144")
    }

    public class func eqMoneyGray() -> UIColor {
        return UIColor(hex: "#757575")
    }
}
